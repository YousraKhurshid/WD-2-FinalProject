export const reducer = (state, action) => {
    switch (action.type) {
        case "ADD_TO_CART":
            return { ...state, cart: [...state.cart, action.cart] };
            case "REMOVE_FROM_CART":
      return {
        ...state,
        cart: state.cart.filter((item, index) => index !== action.payload)
      };
        default:
            return state;
    }
};
